
public class Coordinates {
	
	private double x;
	private double y;
	
	public Coordinates(double x, double y) {
		this.x = x;
		this.y = y;
	}
	
	public double x() {
		return x;
	}
	public void setX(double d) {
		this.x = d;
	}
	public double y() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
}
